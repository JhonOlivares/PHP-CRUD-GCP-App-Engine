CREATE PROCEDURE SP_FILTRAR_AEROPUERTO
@criterio varchar(80)
AS
	BEGIN
	DECLARE @dato varchar(80);
	SELECT @dato = '%'+LTRIM(RTRIM(@criterio))+'%';
	SELECT A.IATA_aeropuertoID, C.nombreCiudad FROM AEROPUERTO AS A INNER JOIN CIUDAD AS C
	ON A.ciudadID=C.ciudadID
	WHERE A.IATA_aeropuertoID LIKE @dato OR A.nombreAeropuerto LIKE @dato OR C.nombreCiudad LIKE @dato
	END
GO


CREATE PROCEDURE SP_SELECCIONAR_VUELOS
@fecha date,
@origen char(3),
@destino char(3)
AS
	BEGIN
	SELECT V.numeroVuelo,
	V.fechaVuelo,
	AO.IATA_aeropuertoID AS IATA_AeropuertoOrigen,
	CO.nombreCiudad AS ciudadOrigen,
	AD.IATA_aeropuertoID AS IATA_AeropuertoDestino,
	CD.nombreCiudad AS ciudadDestino,
	AO.nombreAeropuerto AS nombreAeropuertoOrigen,
	AD.nombreAeropuerto AS nombreAeropuertoDestino,
	VP.horaSalida,
	VP.tiempoDeVuelo FROM VUELO AS V
	INNER JOIN VUELO_PROGRAMADO AS VP ON V.numeroVuelo=VP.numeroVuelo
	INNER JOIN AEROPUERTO AS AO ON VP.origen=AO.IATA_aeropuertoID
	INNER JOIN AEROPUERTO AS AD ON VP.destino=AD.IATA_aeropuertoID
	INNER JOIN CIUDAD CO ON AO.ciudadID= CO.ciudadID
	INNER JOIN CIUDAD CD ON AD.ciudadID=CD.ciudadID
	WHERE V.fechaVuelo = @fecha AND VP.origen = @origen AND VP.destino = @destino
	END
GO

